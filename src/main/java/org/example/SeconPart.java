package org.example;

public class SeconPart {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("The value of x is " + x);

        System.out.println("Various ways to increment x by 1: ");

        x++;
        System.out.println(x);

        ++x;
        System.out.println(x);

        x+=1;
        System.out.println(x);

    }
}
